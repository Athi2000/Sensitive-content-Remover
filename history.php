<h3>World War I</h3>
<p>World War I, also known as the Great War, began in 1914 after the assassination of Archduke Franz Ferdinand of Austria. His murder catapulted into a war across Europe that lasted until 1918. During the conflict, Germany, Austria-Hungary, Bulgaria and the Ottoman Empire (the Central Powers) fought against Great Britain, France, Russia, Italy, Romania, Canada, Japan and the United States (the Allied Powers). Thanks to new military technologies and the horrors of trench warfare, World War I saw unprecedented levels of carnage and destruction. By the time the war was over and the Allied Powers claimed victory, more than 16 million people—soldiers and civilians alike—were dead.</p>
</br>
<h3>World War II</h3>
<p>The instability created in Europe by the First World War (1914-18) set the stage for another international conflict—World War II—which broke out two decades later and would prove even more devastating. Rising to power in an economically and politically unstable Germany, Adolf Hitler, leader of the Nazi Party, rearmed the nation and signed strategic treaties with Italy and Japan to further his ambitions of world domination. Hitler’s invasion of Poland in September 1939 drove Great Britain and France to declare war on Germany, marking the beginning of World War II. Over the next six years, the conflict would take more lives and destroy more land and property around the globe than any previous war. Among the estimated 45-60 million people killed were 6 million Jews murdered in Nazi concentration camps as part of Hitler’s diabolical “Final Solution,” now known as the Holocaust.</p><br>



