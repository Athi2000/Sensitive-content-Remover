<h3>Digestive System</h3>
<p>The digestive system includes the mouth, pharynx (throat), esophagus, stomach, small intestine, large intestine, rectum, and anus. It also includes the salivary glands, liver, gallbladder, and pancreas, which make digestive juices and enzymes that help the body digest food and liquids.</p><br>
<h3>Nervous System</h3>
<p>The nervous system has two main parts: The central nervous system is made up of the brain and spinal cord. The peripheral nervous system is made up of nerves that branch off from the spinal cord and extend to all parts of the body.</p><br>
<h3>Respiratory system</h3>
<p>The respiratory system is the network of organs and tissues that help you breathe. It includes your airways, lungs and blood vessels. The muscles that power your lungs are also part of the respiratory system. These parts work together to move oxygen throughout the body and clean out waste gases like carbon dioxide.</p><br>

