<h3>What is active voice, what is passive voice, and what's the difference?</h3>
<p> In the active voice, the sentence's subject performs the action on the action's target. In the passive voice, the target of the action is the main focus, and the verb acts upon the subject. </p><br>
<h3>When should you use active vs. passive voice?</h3>
<p>Use the active voice in any sentence that focuses on the doer of the action. Unless the majority of your writing is scientific or reporting incidents involving unknown perpetrators, most of the sentences you write should be in the active voice.
<br>The passive voice is meant for sentences where you need to emphasize the target of an action or the action itself rather than who or what is performing the verb. </p><br>
<h3>How do you change passive voice to active? </h3>
<p>To change the passive voice to the active voice, determine who is actually performing the action in the sentence, then restructure the sentence so that the performer is the focus, clearly performing the verb upon the sentenceâ€™s direct object. </p><br>
